using System.Diagnostics.CodeAnalysis;

namespace ArchLucid.Cli.Commands;

[ExcludeFromCodeCoverage(Justification = "CLI prints static template text only.")]
internal static class PilotSuccessCriteriaTemplateCommand
{
    public static Task<int> RunAsync()
    {
        const string md = """
            # ArchLucid pilot — success criteria (copy into your charter)

            **Objective:** Validate that ArchLucid reduces architecture review cycle time and produces a durable, auditable golden manifest in your tenant.

            **Scope window:** __________ (start / end dates)

            **Pilot owner (customer):** __________

            **Technical owner (customer):** __________

            ## Success criteria (check all that apply / targets)

            1. **Time-to-first committed manifest:** ≤ _______ business days from SQL + Entra access.
