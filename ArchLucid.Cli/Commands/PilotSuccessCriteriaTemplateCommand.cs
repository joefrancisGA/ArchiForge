            5. **Stakeholder read-out:** Sponsor receives first-value Markdown or sponsor pack link.

            ## Out of scope for this pilot (explicit)

            - First-party Jira / ServiceNow connectors (V1.1 candidates per V1_DEFERRED.md)
            - MCP / external agent tool host (V1.1 candidate)
            - Self-serve commerce live keys (V1.1 candidate)

            ## Roll-forward decision

            - **Continue to paid / expand:** criteria met on or before __________
